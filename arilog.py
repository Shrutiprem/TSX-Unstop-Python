x = 10
y = 3
print(x + y)  # Addition
print(x - y)  # Subtraction
print(x * y)  # Multiplication
print(x / y)  # Division (float)
print(x // y) # Floor division (integer result)
print(x % y)  # Modulus (remainder)
print(x ** y) # Exponentiation
a = True
b = False
print(a and b)  # False
print(a or b)   # True
print(not a)    # False
